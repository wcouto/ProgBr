// ==
var a = 2;
var b = "2";
console.log(a == b); // Compara valor
console.log(a === b); // Compara tipo

// !=
var c = 5;
var d = "5";
console.log( c != d); // Compara valor
console.log( c !== d); // Compara tipo

// > ou >=
var e = 9;
var f = 3;
console.log( e > f)

// < ou <=
var g = 9;
var h = 3;
console.log( g < h)
