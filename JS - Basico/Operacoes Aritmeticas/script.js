var a = 10;
var b = 15;

// Soma
var soma = a + b;

console.log(soma);

// Subtracao
var subtracao = a - b;

console.log(subtracao);

// Multiplicacao
var multiplicacao = a * b;

console.log(multiplicacao);

// Divisao
var divisao = a / b;

console.log(divisao);

// Modulo - Resto da divisao
var modulo = a % b;

console.log(modulo);

// Incremento
// ++ no inicio incrementa primeiro recebe depois
// ++ no final recebe primeiro incrementa depois
var incremento = ++a;

console.log(incremento);

// Decremento
// -- no inicio decrementa primeiro recebe depois
// -- no final recebe primeiro decrementa depois
var decremento = --b;

console.log(decremento);